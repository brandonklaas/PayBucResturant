    private void signinBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signinBtnActionPerformed
        // TODO add your handling code here:
        if(usernameTf.getText().isEmpty() || new String(passwordTf.getPassword()).isEmpty()){
            errorLbl.setText("Enter Username And Password");
        } else if (usernameTf.getText().equals("Admin") && new String(passwordTf.getPassword()).equals("Admin")){
            Account admin = new Account();
            admin.setUsername("Admin");
            admin.setPassword("Admin");
            
            admin.setAdmin(true);
            admin.setGuest(false);
            admin.setTransactions(true);
            admin.setServices(true);
            admin.setProducts(true);
            admin.setEmployees(true);
            admin.setSettings(true);
            admin.setAccounts(true);
                        
            session.setLoggedInUser(admin);
            this.dispose();
            Loading loading = new Loading(null, false, session);
            loading.startThread();
        
        } else if(validateAccount(usernameTf.getText(), new String(passwordTf.getPassword()))){
            session.setLoggedInUser(loggedInUserAccount);
            this.dispose();
            Loading loading = new Loading(null, false, session);
            loading.startThread();
            
        } else {
            errorLbl.setText("Incorrect Username And Password");
        }
        
    }//GEN-LAST:event_signinBtnActionPerformed

