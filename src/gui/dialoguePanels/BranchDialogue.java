    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:        
        if(branchNameTf.getText().isEmpty() == false && branchCodeTf.getText().isEmpty() == false){
            Branch branch = new Branch();
            branch.setName(branchNameTf.getText());
            branch.setBranchNo(branchCodeTf.getText());
            branch.setAddress(address.getText());
            
            if(database.insert(branch)){
                clearPanels();
                panel.refreshTable();
                diag.setVisible(false);
                new OkayDialogue(null, true, "Branch saved successfully");
                diag.dispose();
            } else {
                new OkayDialogue(null, true, "Failed to save Branch");
            }
        }
    }//GEN-LAST:event_saveBtnActionPerformed

