    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        if(branchNameTf.getText().isEmpty() == false && branchCodeTf.getText().isEmpty() == false){
            Branch branch = new Branch();
            branch.setId(selectedBranch.getId());
            branch.setName(branchNameTf.getText());
            branch.setBranchNo(branchCodeTf.getText());
            branch.setAddress(address.getText());
            
            if(database.update(branch)){
                diag.dispose();
                panel.refreshTable();
                diag.setVisible(false);
                new OkayDialogue(null, true, "Branch updated successfully");
                diag.dispose();
            } else {
                new OkayDialogue(null, true, "Failed to update Branch");
            }
        }
    }//GEN-LAST:event_updateBtnActionPerformed


